package com.protocol.webAPIResponse;

public class ObjectEvent {
	int id;
	int type;
	String stat;
	String ezn;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
	public String getStat() {
		return stat;
	}
	public void setStat(String stat) {
		this.stat = stat;
	}
	
	public String getEzn() {
		return ezn;
	}
	public void setEzn(String ezn) {
		this.ezn = ezn;
	}
	
	

}
